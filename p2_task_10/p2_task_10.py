String = "Запомнить"
print('1 line: ' , String[2]) # третий символ в строке
print('2 line: ' , String[1:]) # символ с второго до последнего 
print('3 line: ' , String[0:6]) # первые 5 символов
print('4 line: ' , String[:-2]) # все кроме последних двух символов
print('5 line: ' , String[::2]) # все четные символы
print('6 line: ' , String[1::2]) # все нечетные символы
print('7 line: ' , String[::-1]) # символы в обратном порядке
print('8 line: ' , String[::-3]) # каждый второй в обратном порядке, начиная с последнего 
print('9 line: ' , len(String))  #длина стринга
